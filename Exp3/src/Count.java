public class Count {

    private double featureVectorCount;
    private double contextWordsCount;

    public double getFeatureVectorCount() {
        return featureVectorCount;
    }

    public void setFeatureVectorCount(double featureVectorCount) {
        this.featureVectorCount = featureVectorCount;
    }

    public double getContextWordsCount() {
        return contextWordsCount;
    }

    public void setContextWordsCount(double contextWordsCount) {
        this.contextWordsCount = contextWordsCount;
    }
}
